package mut.report;

public class OverallStatistics {

	private int numberOfMutants = 0;

	private int numberOfSurvivors = 0;

	public int getNumberOfMutants() {
		return numberOfMutants;
	}

	public void setNumberOfMutants(int numberOfMutants) {
		this.numberOfMutants = numberOfMutants;
	}

	public int getNumberOfSurvivors() {
		return numberOfSurvivors;
	}

	public void setNumberOfSurvivors(int numberOfSurvivors) {
		this.numberOfSurvivors = numberOfSurvivors;
	}

}
